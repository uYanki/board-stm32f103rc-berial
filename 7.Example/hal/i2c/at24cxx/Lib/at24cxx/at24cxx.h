// 3位用于设备地址，0位用于内存寻址：可挂载8个；
// 2位用于设备地址，1位用于内存寻址：可挂载4个；
// 1位用于设备地址，2位用于内存寻址：可挂载2个；
// 0位用于设备地址，3位用于内存寻址：可挂载1个；

