// 二线制串行（I2C）EEPROM AT24CXX 系列的操作具有 1K、2K、4K、8K、16K、32K、64K、
// 128K、256K、512K、1M、2M 等多种容量，不同容量其采用的设备地址位、寄存器地址位
// 存在差异每一页所包含的字节数也不一样。

